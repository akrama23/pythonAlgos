# Predefined list of numbers
predef_list = [4, -27, 15, 33, -10]
    
# Accept an integer input
input_value = int(input())
    
# Find the maximum value in the predefined list
max_value = max(predef_list)
    
# Check if the input value is greater than the maximum value in the list
is_greater = input_value > max_value
    
# Output the result
print(f"Greater Than Max? {is_greater}")
    
    